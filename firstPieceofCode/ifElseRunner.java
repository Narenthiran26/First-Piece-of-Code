package com.naren.firstPieceofCode;

public class ifElseRunner {

	public static void main(String[] args) {
		int i = 22;
		
		if (i==25) {
			System.out.println("i=25");
		}else if (i==24) {
			System.out.println("i=24");
		}else if (i==23) {
			System.out.println("i=23");
		}
		else {
			System.out.println("i!=23,i!=24,i!=25");
		}

	}

}
