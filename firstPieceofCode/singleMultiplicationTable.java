package com.naren.firstPieceofCode;

public class singleMultiplicationTable {

	public void table() {
			for(int i=1;i<=10;i++) {
			System.out.printf("%d * %d = %d",5,i,i*5).println();
					
			}
		}
	public void table(int t) {
		for (int i=1;i<=t;i++) {
			System.out.printf("%d * %d = %d",t,i,i*t).println();
		}
	}

	}
